# THere are 2 type conversions:

# 1. Type Conversion:  (Automatically converts the type by python interpreter)

a = 2
b = 2.25
print("Answer by Type Conversion method :", a + b)

# 2. Type Casting: (Needs to convert the type manually)

a = int("2")
b = 2.25
print("Answer by Type Casting method :", a + b)
